# O que é uma pilha (stack)
# Coleção ordenada de itens
# FILO - First in, Last Out
# Comandos para pilha em python:
# Stack() cria uma nova pilha vazia. Não necessita parâmetros e retorna uma pilha vazia.
# push(item) insere um novo item na pilha. A operação necessita o item e não retorna coisa alguma.
# pop() remove o item que está no topo da pilha. Não necessita parametros e retorna o item removido.
# peek() retorna o item no topo da pilha mas não o remove da pilha. Não necessita de parâmetros. Não é modificada.
# isEmpty() testa se a pilha está vazia. Não necessita parâmetros e retorna um valor booleano.
# size() retorna o número de item na pilha. Não necessita parâmetros e retorna um inteiro; valor do tipo int.
